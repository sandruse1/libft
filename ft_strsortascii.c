#include "libft.h"

void	ft_strsortascii(char *s)
{
	int i;
	int j;

	i = 0;
	j = ft_strlen(s);
	while (j >= 0)
	{
		while (s[i + 1])
		{
			if (s[i] > s[i + 1])
				ft_swap(s, i, i + 1);
			i++;
		}
		i = 0;
		j--;
	}
}
